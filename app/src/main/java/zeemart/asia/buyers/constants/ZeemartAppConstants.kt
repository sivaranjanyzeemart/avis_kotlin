            Language.ENGLISH
        );

        fun isMarket(market: Market): Boolean {
            return market.countryCode.equals(countryCode, ignoreCase = true)
        }

        val locale: Locale
            get() = Locale(langCode, countryCode)

        fun getTimeZone(): TimeZone {
            return TimeZone.getTimeZone(timeZone)
        }

        companion object {
            val `this`: Market
                get() {
                    try {
                        if (SharedPref.buyerDetail?.market.equals(
                                INDONESIA.countryCode,
                                ignoreCase = true
                            )
                        ) {
                            return INDONESIA
                        } else if (SharedPref.buyerDetail?.market.equals(
                                VIETNAM.countryCode,
                                ignoreCase = true
                            )
                        ) {
                            return VIETNAM
                        } else if (SharedPref.buyerDetail?.market.equals(
                                MALAYSIA.countryCode,
                                ignoreCase = true
                            )
                        ) {
                            return MALAYSIA
                        } else if (SharedPref.buyerDetail?.market.equals(
                                UAE.countryCode,
                                ignoreCase = true
                            )
                        ) {
                            return UAE
                        } else if (SharedPref.buyerDetail?.market.equals(
                                AUSTRALIA.countryCode,
                                ignoreCase = true
                            )
                        ) {
                            return AUSTRALIA
                        } else if (SharedPref.buyerDetail?.market.equals(
                                SINGAPORE.countryCode,
                                ignoreCase = true
                            )
                        ) {
                            return SINGAPORE
                        }
                    } catch (e: Exception) {
                        e.printStackTrace()
                    }
                    return SINGAPORE
                }
        }
    }

